using UnityEngine;

public class BucketKinematics : MonoBehaviour
{
    [Header("References")]
    [Tooltip("???? ????????? (PaintBucket) ????? ???")]
    public Transform paintBucket;

    [Header("Spherical Pendulum Physics")]
    public float ropeLength = 3f;
    public float gravity = 9.81f;

    [Header("Initial Conditions")]
    [Tooltip("??????? ??????? (Theta): ????? ????? ?? ???????")]
    public float startThetaDegrees = 45f;
    [Tooltip("?????? ??????? (Phi Velocity): ???? ?????? ??????? ?? 3D")]
    public float startPhiVelocity = 1.5f;

    [Header("Fluid Dynamics Outputs")]
    public Vector3 effectiveGravity;

    private float theta;
    private float phi;
    private float thetaVelocity;
    private float phiVelocity;

    private Vector3 currentVelocity;
    private Vector3 lastPosition;

    void Start()
    {
        theta = startThetaDegrees * Mathf.Deg2Rad;
        phi = 0f;
        thetaVelocity = 0f;
        phiVelocity = startPhiVelocity;

        lastPosition = CalculateCartesianPosition(theta, phi);

        // ??? ????????? ?? ???? ??????? ???? ????? ???????
        if (paintBucket != null)
        {
            paintBucket.localPosition = lastPosition;
        }
    }



    [Header("Air Drag & Damping")]
    [Tooltip("???? ??????? ????? ?????? ?????? ????? ?????? ??????? (NaN)")]
    public float damping = 0.995f;

    void Update()
    {
        if (paintBucket == null) return;

        float thetaAcceleration = (phiVelocity * phiVelocity) * Mathf.Sin(theta) * Mathf.Cos(theta) - (gravity / ropeLength) * Mathf.Sin(theta);

        float phiAcceleration = 0f;
        // ???? ?? ?????? ???? ?????? ??? ??? (???? ??? NaN)
        if (Mathf.Abs(Mathf.Sin(theta)) > 0.05f)
        {
            phiAcceleration = -2f * thetaVelocity * phiVelocity * (Mathf.Cos(theta) / Mathf.Sin(theta));
        }

        thetaVelocity += thetaAcceleration * Time.deltaTime;
        phiVelocity += phiAcceleration * Time.deltaTime;

        // --- ????? ?????? ?????? (???????) ???! ---
        thetaVelocity *= damping;
        phiVelocity *= damping;

        theta += thetaVelocity * Time.deltaTime;
        phi += phiVelocity * Time.deltaTime;

        // ... (???? ????? ??? ?? ???? ?????)

        // 2. ????? ???? ????????? ?? ?????? ????? ???????
        Vector3 newPosition = CalculateCartesianPosition(theta, phi);
        paintBucket.localPosition = newPosition;

        // 3. ????? ????????? ????? ?????? ?????? ?????
        if (newPosition != Vector3.zero)
        {
            paintBucket.up = -newPosition.normalized;
        }

        // 4. ???? ???????? ???????
        Vector3 velocity = (newPosition - lastPosition) / Time.deltaTime;
        Vector3 acceleration = (velocity - currentVelocity) / Time.deltaTime;

        currentVelocity = velocity;
        lastPosition = newPosition;

        Vector3 realGravity = new Vector3(0, -gravity, 0);
        effectiveGravity = realGravity - acceleration;
    }

    Vector3 CalculateCartesianPosition(float t, float p)
    {
        float x = ropeLength * Mathf.Sin(t) * Mathf.Cos(p);
        float z = ropeLength * Mathf.Sin(t) * Mathf.Sin(p);
        float y = -ropeLength * Mathf.Cos(t);
        return new Vector3(x, y, z);
    }
}